package TpIntroduccion.ejercicio3;

public class Ejercicio3 {

    public static void main (String[] args) {
        int N = 26;
        int Resultado;

        while (N < 78) {
            int i = 1;
            N = N + 1;
            i++;
        }
        System.out.println(N);

        while (N > 3) {
            int i = 1;
            N = N - 1;
            i++;
        }
        System.out.println(N);

        Resultado = N * 3;
        System.out.println(Resultado);



    }
}
