package TpIntroduccion.ejercicio2;

public class Ejercicio2 {

    public static void main (String[] args) {
         int X = 3 , Y = 6;
         double N = 20, M = 9;

         double sumar = X + M;
         double restar = N + Y;
         double multiplicar = N * X;
         double dividir  = M / Y;

        System.out.println("Valor de la suma: 3 + 9 = " + sumar );
        System.out.println("Valor de la restar: 20 - 6 = " + restar );
        System.out.println("Valor de la multiplicar: 20 * 3 = " + multiplicar );
        System.out.println("Valor de la dividir: 9 / 6 = " + dividir );



    }
}
